    public void searchAndFillTable(){
     try {
            DefaultTableModel mod = (DefaultTableModel)jTable1.getModel();
            mod.setRowCount(0);
            Database db = Connection.openConnection(myDB);
            ArrayList<Journals> journ = (ArrayList<Journals>) db.getList("Journal");
            String nam = search.getText();
            if(journ !=null){
                for(Journals c : journ){
                    if(c.getTitle().toLowerCase().startsWith(nam.toLowerCase())){
                        mod.addRow(new Object[]{c.getDate(), c.getTitle(),c.getEntry()});
                        jTable1.getColumnModel().getColumn(2).setMaxWidth(0);
                        
                    }
                }
            }
            
        } catch (IOException ex) {
            ex.printStackTrace();
        } catch (ClassNotFoundException ex) {
            ex.printStackTrace();
        }
    
    
    
    
    
    }
