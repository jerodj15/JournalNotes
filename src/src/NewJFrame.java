    private void saveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveActionPerformed
        Journals con = new Journals(date.getText(),title.getText(),entry.getText());
        try {
        Database db = Connection.openConnection(myDB);
            ArrayList<Journals> journ = (ArrayList<Journals>) db.getList("Journal");
            if(journ==null){
            journ = new ArrayList<>();
            db.addList("Journal", journ);
        
        }
            journ.add(con);
            Connection.save();
        } catch (IOException ex){
            ex.printStackTrace();
        } catch (ClassNotFoundException ex){
        ex.printStackTrace();
        }
        searchAndFillTable();ClearM();
    }//GEN-LAST:event_saveActionPerformed

